



def sum(x: int):
    """
    this func sum of given integer
    :param x:
    :return x+x:
    """
    return x + x

def power(x: int):
    return x ** 2

def concatenation(x: str):
    return x + x

def subtraction_by_1(x: float):
    return x-1

def slice(string:str, start: int, end: int) -> str:
    return string[start:end]